public class Stock {
    String item;
    int quantity;
    int ppitem;

    public String getItem() {
        return item;
    }

    public void setItem(String item) {
        this.item = item;
    }

    public int getQuantity() {
        return quantity;
    }

    public void setQuantity(int quantity) {
        this.quantity = quantity;
    }

    public int getPpitem() {
        return ppitem;
    }

    public void setPpitem(int ppitem) {
        this.ppitem = ppitem;
    }
}
